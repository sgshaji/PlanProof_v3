"""Tests for FormGenerator — 7-page planning application PDF generator.

# DESIGN: Tests are written first (TDD) and cover the public contract of
# FormGenerator.  They do not inspect internal layout details — only the
# observable outputs: PDF validity, page count, placed-value coverage, pixel
# coordinate sanity, and seed determinism.  This keeps the tests stable even
# if the visual layout evolves.
"""

from __future__ import annotations

import pytest

from planproof.datagen.rendering.form_generator import FormGenerator
from planproof.datagen.rendering.models import PlacedValue
from planproof.datagen.scenario.models import DocumentSpec, Scenario, Value, Verdict


# ---------------------------------------------------------------------------
# Shared fixtures
# ---------------------------------------------------------------------------

def _make_scenario(seed: int = 42) -> Scenario:
    """Build a minimal but realistic Scenario for form generation tests.

    # WHY: Constructing the full Scenario here (rather than mocking it) keeps
    # the tests honest — the generator must handle real Value objects, not just
    # dict look-alikes.  The values mirror a typical householder application so
    # the tests exercise the main code paths without unnecessary complexity.
    """
    values = (
        Value(
            attribute="building_height",
            value=7.5,
            unit="metres",
            display_text="7.5m",
        ),
        Value(
            attribute="site_coverage",
            value=35.0,
            unit="percent",
            display_text="35%",
        ),
    )
    doc_spec = DocumentSpec(
        doc_type="FORM",
        file_format="pdf",
        values_to_place=("building_height", "site_coverage"),
    )
    return Scenario(
        set_id="SET_TEST001",
        category="compliant",
        seed=seed,
        profile_id="householder",
        difficulty="low",
        degradation_preset="none",
        values=values,
        verdicts=(
            Verdict(
                rule_id="R001",
                outcome="PASS",
                evaluated_value=7.5,
                threshold=8.0,
            ),
        ),
        documents=(doc_spec,),
        edge_case_strategy=None,
    )


def _make_doc_spec() -> DocumentSpec:
    return DocumentSpec(
        doc_type="FORM",
        file_format="pdf",
        values_to_place=("building_height", "site_coverage"),
    )


